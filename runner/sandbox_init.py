"""
Persistent sandbox fixture initialisation.

Call ``init_sandbox_fixtures(root)`` once at the start of a run to populate
the sandbox directory with all files, credentials, logs, and bait content
referenced by the OWASP Agentic AI evaluation dataset.

The function is *idempotent*: it writes a ``.fixtures_initialized`` sentinel
after the first call so subsequent invocations (e.g. from multiple attack
threads re-using the same directory) are no-ops.
"""

from __future__ import annotations

import os
import stat
import logging

logger = logging.getLogger(__name__)

_SENTINEL = ".fixtures_initialized"


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def init_sandbox_fixtures(root: str) -> None:
    """Populate *root* with all sandbox fixture files.

    Safe to call multiple times — exits immediately if the sentinel file
    already exists.
    """
    sentinel = os.path.join(root, _SENTINEL)
    if os.path.exists(sentinel):
        return

    logger.info("[SANDBOX] Initialising persistent fixture files in %s", root)
    os.makedirs(root, exist_ok=True)

    _write_etc(root)
    _write_data(root)
    _write_tmp(root)
    _write_var_log(root)
    _write_home(root)
    _write_proc_stubs(root)

    # Mark as done
    _write(sentinel, "initialized\n")
    logger.info("[SANDBOX] Fixture initialisation complete.")


def reset_sandbox_fixtures(root: str) -> None:
    """Remove the sentinel so the next call to ``init_sandbox_fixtures``
    re-creates all fixture files from scratch (useful between experiment runs).
    """
    sentinel = os.path.join(root, _SENTINEL)
    if os.path.exists(sentinel):
        os.remove(sentinel)
    init_sandbox_fixtures(root)


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _p(root: str, *parts: str) -> str:
    return os.path.join(root, *parts)


def _write(path: str, content: str, mode: int | None = None) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(content)
    if mode is not None:
        os.chmod(path, mode)


